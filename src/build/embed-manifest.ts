import { resolve } from "node:path";

/**
 * Writes `with { type: "file" }` embed manifest so Bun can pack `workerd` and the Wrangler bundle into a native binary.
 */
export async function writeEmbedManifest(
	workerdAbs: string,
	bundleAbs: string,
	outPath: string,
): Promise<void> {
	const workerdJson = JSON.stringify(resolve(workerdAbs));
	const bundleJson = JSON.stringify(resolve(bundleAbs));
	await Bun.write(
		outPath,
		`// Generated by worker-native — do not edit.
import embeddedWorkerdPathImport from ${workerdJson} with { type: "file" };
import embeddedWorkerBundlePathImport from ${bundleJson} with { type: "file" };

/** Bun path (often \`$bunfs/…\` when running a compiled executable). */
export const embeddedWorkerdPath: string = embeddedWorkerdPathImport;

/** Wrangler bundle path (embedded into the executable). */
export const embeddedWorkerBundlePath: string = embeddedWorkerBundlePathImport;
`,
	);
}
