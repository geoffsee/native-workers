import { mkdir } from "node:fs/promises";
import { dirname } from "node:path";

/**
 * Generates the Bun.compile entry that wires embed paths into the Miniflare host.
 */
export async function writeCompileGateway(
	outPath: string,
	options?: { manifestImportPath?: string },
): Promise<void> {
	const manifestImport = options?.manifestImportPath ?? "./embed-manifest";

	await mkdir(dirname(outPath), { recursive: true });

	const body = `// Generated by worker-native — do not edit.
import {
	embeddedWorkerBundlePath,
	embeddedWorkerdPath,
} from "${manifestImport}";
// Set before Miniflare (and its workerd resolution) loads:
process.env.MINIFLARE_WORKERD_PATH = embeddedWorkerdPath;
const { runMiniflareHost } = await import("worker-native/host");
await runMiniflareHost({
	embeddedWorkerdPath,
	embeddedWorkerBundlePath,
});
`;

	await Bun.write(outPath, body);
}
